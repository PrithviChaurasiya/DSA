package DSA.Array;

public class ForEachLoop {
    public static void main(String[] args) {
        int[] arr = {1,2,3,4,5,6,7};
        for (int i : arr) {
            System.out.print(i + " ");
        }
    }
}
 //for each loop is used to traverse the data structures
 //we cannot update the values of an array using for each loop