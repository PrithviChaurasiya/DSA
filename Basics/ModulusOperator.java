package DSA.Basics;

public class ModulusOperator {
    public static void main(String[] args) {
        System.out.println(10%8);
        System.out.println(8%10);
        System.out.println(-10%8); //-(10%8)
        System.out.println(10%-8); //10%8
        System.out.println(-39%-10);//-(39%10)
    }
}
