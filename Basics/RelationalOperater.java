package DSA.Basics;

public class RelationalOperater {
    public static void main(String[] args) {
        System.out.println(5>9);
        System.out.println(5<9);
        System.out.println(5<=5);
        System.out.println(5<5);
        System.out.println(5>5);
        System.out.println(5==5);
        System.out.println(5==9);
        System.out.println(5!=7);
    }
}
