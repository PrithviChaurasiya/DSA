package DSA.Basics;

public class Division_Operator {
    public static void main(String[] args) {
        double x = 395;
        double y = 10;
        System.out.println(x/y);
        System.out.println(5/y);
        System.out.println(x/10);
        System.out.println(98/10);
        System.out.println(98/10.0);
        System.out.println(9.8/10);
        
    }
}
