package DSA.Basics;

public class IncrementAndDecrement {
    public static void main(String[] args) {
        int x = 53;
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x++);

        int y = 67;
        System.out.println(y);
        System.out.println(y--);
        System.out.println(--y);
    }
}
